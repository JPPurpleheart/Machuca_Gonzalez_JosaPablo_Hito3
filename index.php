        <h3>Iglesia Cristiana Evangélica Horeb es una comunidad cristiana en el Polígono San Pablo sita en la calle
            Doctor Laffon Soto s/n frente al Polideportivo San Pablo. Como comunidad cristiana, buscamos llevar la
            palabra de Dios a todas las personas como está escrito: "Ustedes vayan y hagan más discípulos míos en todos
            los países de la tierra. Bautícenlos en el nombre del Padre, del Hijo y del Espíritu Santo. Enséñenles a
            obedecer todo lo que yo les he enseñado. Yo estaré siempre con ustedes, hasta el fin del mundo." - Mateo
            28:19-20 Traducción en Lenguaje Actual, adicionalmente la iglesia cuenta con un ministerio de Despensa y
            Ropero donde se apoya a los más necesitados con comida y ropa para todos aquellos que verdaderamente lo
            necesiten y una reunión semanal los sábados exclusiva para los jóvenes con hermanos y hermanas de Nueva Sión
            y Manantial de Vida entre otras Iglesias de la ciudad.</h3>
